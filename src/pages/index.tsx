export const getStaticProps: GetStaticProps = async () => {
  const price = await stripe.prices.retrieve('price_1Lz6M0HOKwH0vkwQvoxyYZJ1', {
    expand: ['product'],
  });

  const product = {
    priceId: price.id,
    amount: new Intl.NumberFormat('en-US', {
      style: 'currency',
      currency: 'USD',
    }).format((price.unit_amount as number) / 100),
  };
  return {
    props: {
      product,
    },
    revalidate: 60 * 60 * 24,
  };
};
